package com.xworkz.patient;

public class Patient {
	private String name;
	private int age;
	private String gender;
	private String address;
	private int patientId;
	private String diseaseName;
	private String consultingDoctorName;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String adress) {
		this.address = adress;
	}
	public int getPatientId() {
		return patientId;
	}
	public void setPatientId(int patientId) {
		this.patientId = patientId;
	}
	public String getDiseaseName() {
		return diseaseName;
	}
	public void setDiseaseName(String diseaseName) {
		this.diseaseName = diseaseName;
	}
	public String getConsultingDoctorName() {
		return consultingDoctorName;
	}
	public void setConsultingDoctorName(String consultingDoctorName) {
		this.consultingDoctorName = consultingDoctorName;
	}
	
	
	

}
